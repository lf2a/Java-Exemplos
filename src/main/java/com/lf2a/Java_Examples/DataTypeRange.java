package com.lf2a.Java_Examples;

public class DataTypeRange {
	public static void main(String[] args) {
		System.out.println("Max: " + Double.MAX_VALUE + "\nMin: " + Double.MIN_VALUE);
		System.out.println("");
		System.out.println("Max: " + Float.MAX_VALUE + "\nMin: " + Double.MIN_VALUE);
		System.out.println("");
		System.out.println("Max: " + Short.MAX_VALUE + "\nMin: " + Short.MIN_VALUE);
		System.out.println("");
		System.out.println("Max: " + Byte.MAX_VALUE + "\nMin: " + Byte.MIN_VALUE);
		System.out.println("");
		System.out.println("Max: " + Integer.MAX_VALUE + "\nMin: " + Integer.MIN_VALUE);
		System.out.println("");
		System.out.println("Max: " + Long.MAX_VALUE + "\nMin: " + Long.MIN_VALUE);
	}
}
